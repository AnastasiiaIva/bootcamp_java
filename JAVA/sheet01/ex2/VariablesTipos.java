package JAVA.sheet01.ex2;

public class VariablesTipos {
    public static void main(String[] args) {
        int num;
        double dob;
        boolean bool;
        char ch;
        String str;

        num = 4;
        dob = 24E3d;
        bool = true;
        ch = 'A';
        str = "hola";

        System.out.println(num);
        System.out.println(dob);
        System.out.println(bool);
        System.out.println(ch);
        System.out.println(str);
    }
}
